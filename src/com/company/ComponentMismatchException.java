public class ComponentMismatchException extends Exception {
    public ComponentMismatchException() {super();}
    public ComponentMismatchException(String message) {super(message);}
    public ComponentMismatchException(String message, Throwable cause) {super(message, cause);}
    public ComponentMismatchException(Throwable cause) {super(cause);}
}
